CREATE TABLE COMPANY1 (
	COMPANYID INTEGER,
	COMPANYNAME VARCHAR(255),
	COMPANYADDRESS VARCHAR(255),
	LOC BOOLEAN,
	SECTORTYPE VARCHAR(255),
	CONSTRAINT PK_COMPANY1 PRIMARY KEY (COMPANYID));
	
CREATE TABLE EMPLOYERDETAILS1 (
	EMPLOYERID INTEGER,
	EMPLOYERNAME VARCHAR(255),
	ADDRESS VARCHAR(255),
	EMAIL VARCHAR(255),
	COMPANYID INTEGER,
	EMPLOYEEID INTEGER,
	CONSTRAINT FK_COMPANYID FOREIGN KEY (COMPANYID) REFERENCES COMPANY1 (COMPANYID),
	CONSTRAINT FK_EMPLOYEEID FOREIGN KEY (EMPLOYEEID) REFERENCES EMPLOYMENTDETAILS1 (EMPLOYEEID),
	CONSTRAINT PK_EMPLOYERDETAILS1 PRIMARY KEY (EMPLOYERID));

CREATE TABLE EMPLOYMENTDETAILS1 (
	EMPLOYEEID INTEGER,
	EMPLOYMENTTYPE VARCHAR(255),
	GRADROLE BOOLEAN,
	EMPLOYMENTSTARTDATE DATE,
	JOBTITLE VARCHAR(255),
	DESCRIPTIONOFROLE VARCHAR(255),
	PUBLICITYSTATUS BOOLEAN,
	STUDENTID INTEGER,
	CONSTRAINT FK_STUDENTID FOREIGN KEY (STUDENTID) REFERENCES STUDENT1 (STUDENTID),
	CONSTRAINT PK_EMPLOYEMENTDETAILS1 PRIMARY KEY (EMPLOYEEID));
	
CREATE TABLE STUDENT1(
	STUDENTID INTEGER,
	STUDENTNAME VARCHAR(255),
	STUDENTEMAIL VARCHAR(255),
	FINALPERCENTAGE DOUBLE,
	EXISTINGSTATUS VARCHAR(255),
	CONSTRAINT PK_STUDENT1 PRIMARY KEY (STUDENTID));

CREATE TABLE PROGRAMME1(
	PROGRAMMEID VARCHAR(255),
	PROGRAMMENAME VARCHAR(255),
	FEEDBACK VARCHAR(255),
	PROGRAMMETYPE VARCHAR(255),
	COMPLETIONDATE DATE,
	STUDENTID INTEGER,
	CONSTRAINT FK_STUDENTID FOREIGN KEY (STUDENTID) REFERENCES STUDENT1 (STUDENTID),
	CONSTRAINT PK_PROGRAMME1 PRIMARY KEY (PROGRAMMEID));

CREATE TABLE MODULES1(
	MODULEID VARCHAR(255),
	MODULENAME VARCHAR(255),
	OPTIONAL BOOLEAN,
	PROGRAMMEID VARCHAR(255),
	CONSTRAINT FK_PROGRAMMEID FOREIGN KEY (PROGRAMMEID) REFERENCES PROGRAMME (PROGRAMMEID),
	CONSTRAINT PK_MODULEID PRIMARY KEY (MODULEID));
	
