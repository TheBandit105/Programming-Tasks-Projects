CREATE TABLE DEPT (
  DEPTNO INTEGER,
  DNAME VARCHAR(14),
  LOC VARCHAR(13),
  CONSTRAINT PK_DEPT PRIMARY KEY (DEPTNO));

CREATE TABLE EMP (
  EMPNO INTEGER,
  ENAME VARCHAR(10),
  JOB VARCHAR(9),
  MGR INTEGER,
  HIREDATE DATE,
  SAL INTEGER,
  COMM INTEGER,
  DEPTNO INTEGER,
  CONSTRAINT FK_DEPTNO FOREIGN KEY (DEPTNO) REFERENCES DEPT (DEPTNO),
  CONSTRAINT PK_EMP PRIMARY KEY (EMPNO));

CREATE TABLE BONUS (
  ENAME VARCHAR(10),
  JOB VARCHAR(9),
  SAL INTEGER, COMM INTEGER);

CREATE TABLE SALGRADE (
  GRADE INTEGER,
  LOSAL INTEGER,
  HISAL INTEGER);


